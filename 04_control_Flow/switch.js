const fruits = "watemelon"
switch (fruits) {
    case "apple":
        console.log("i love eating apple")
        break;

    case "pineapple":
        console.log("i love eating pineapple")
        break;

    case "guava":
        console.log("i love eating guava")
        break;

    case "mango":
        console.log("i love eating mango")
        break;

    default: console.log("season has past ");
        break;
}
